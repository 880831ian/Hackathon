              <h1 class="headline"><b>2.個人資料保護法</b></h1>
                <p>本服務將會收集以下個人資料內容、使用法條規章：</p>
                <li>個人資料：姓名、生日、電話號碼、郵件、國籍，地址。 </li>
                <li>個人資料檔案：指依系統建立而得以自動化機器或其他非自動化方式 檢索、整理之個人資料之集合。</li>
                <li>蒐集：指以任何方式取得個人資料。</li>
                <li>處理：指為建立或利用個人資料檔案所為資料之記錄、輸入、儲存、 編輯、更正、複製、檢索、刪除、輸出、連結或內部傳送。</li>
                <li>利用：指將蒐集之個人資料為處理以外之使用。</li>
                <li>國際傳輸：指將個人資料作跨國（境）之處理或利用。</li>
                <li>公務機關：指依法行使公權力之中央或地方機關或行政法人。</li>
                <li>非公務機關：指前款以外之自然人、法人或其他團體。</li>
                <li>當事人：指個人資料之本人。</li>
                <p></p>
                <p>您可以控制自己的個人資料，因此：</p>
                <li>裝置使用權限：您可將設備上的設定變更為同意或反對收集相應的資訊，或顯示相應的通知。當然，如果這麼做，某些服務可能會失去部分功能。</li>
                <li>刪除：您可直接使用服務中的刪除帳號功能，來刪除您的帳號。</li>
                <li>查看您的資訊：適用的隱私法律可能授權您查看我們所保有的您個人資訊（根據司法管轄區而定，這可能稱為存取權、可攜帶權，或其他類似名稱）。</li>
                <li>更新您的資訊：如果您認為我們所保有的您的相關資訊不正確，或者我們不再有權使用該資訊，並且希望要求更正、刪除資訊或反對資訊處理，請與我們聯繫。</li>
                <p></p>
                <p>為了保護您和我們所有的使用者，我們會請您提供身份證明，才能回覆上述要求。</p>
                <li>責任歸屬：如果您對我們如何處理您的個人資訊有所疑慮，您有權向適當的資料保護機構投訴。您可以向您的經常居住地、工作地點或我們成立的地點的資料保護機構投訴。</li>
